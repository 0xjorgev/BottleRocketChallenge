import UIKit

extension UILabel {
    
    func addTitleStyle(){
        
        self.font = Font.nameFont
        
        self.textColor = Color.titleColor
        
    }
    
    
    func addCategoryStyle(){
        
        self.font = Font.categoryFont
        
        self.textColor = Color.titleColor
        
    }
    
